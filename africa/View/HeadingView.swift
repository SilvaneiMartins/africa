import SwiftUI

struct HeadingView: View {
    // MARK: - Propriedade
    
    var headingImage: String
    var headingText: String
    
    // MARK: - Conteudo
    var body: some View {
        HStack {
            Image(systemName: headingImage)
                .foregroundColor(.accentColor)
                .imageScale(.large)
            
            Text(headingText)
                .font(.title3)
                .fontWeight(.bold)
        }//: HSTACK
        .padding(.vertical)
    }
}

